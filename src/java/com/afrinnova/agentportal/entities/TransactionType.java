package com.afrinnova.agentportal.entities;

import java.io.Serializable;
import java.util.Collection;
import java.util.Date;
import javax.persistence.Basic;
import javax.persistence.CascadeType;
import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.NamedQueries;
import javax.persistence.NamedQuery;
import javax.persistence.OneToMany;
import javax.persistence.Table;
import javax.persistence.Temporal;
import javax.persistence.TemporalType;
import javax.validation.constraints.Size;
import javax.xml.bind.annotation.XmlRootElement;
import javax.xml.bind.annotation.XmlTransient;

/**
 *
 * @author Unicorn
 */
@Entity
@Table(name = "transactiontype")
@XmlRootElement
@NamedQueries({
    @NamedQuery(name = "TransactionType.findAll", query = "SELECT t FROM TransactionType t"),
    @NamedQuery(name = "TransactionType.findById", query = "SELECT t FROM TransactionType t WHERE t.id = :id"),
    @NamedQuery(name = "TransactionType.findByTransactiontypename", query = "SELECT t FROM TransactionType t WHERE t.transactiontypename = :transactiontypename"),
    @NamedQuery(name = "TransactionType.findByPercentage", query = "SELECT t FROM TransactionType t WHERE t.percentage = :percentage"),
    @NamedQuery(name = "TransactionType.findByDatecreated", query = "SELECT t FROM TransactionType t WHERE t.datecreated = :datecreated")})
public class TransactionType implements Serializable {
    private static final long serialVersionUID = 1L;
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    @Basic(optional = false)
    @Column(name = "id")
    private Integer id;
    @Size(max = 255)
    @Column(name = "transactiontypename")
    private String transactiontypename;
    @Size(max = 45)
    @Column(name = "percentage")
    private String percentage;
    @Column(name = "datecreated")
    @Temporal(TemporalType.TIMESTAMP)
    private Date datecreated;
    @OneToMany(cascade = CascadeType.ALL, mappedBy = "transactiontypeId")
    private Collection<UserTransactions> userTransactionsCollection;

    public TransactionType() {
    }

    public TransactionType(Integer id) {
        this.id = id;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getTransactiontypename() {
        return transactiontypename;
    }

    public void setTransactiontypename(String transactiontypename) {
        this.transactiontypename = transactiontypename;
    }

    public String getPercentage() {
        return percentage;
    }

    public void setPercentage(String percentage) {
        this.percentage = percentage;
    }

    public Date getDatecreated() {
        return datecreated;
    }

    public void setDatecreated(Date datecreated) {
        this.datecreated = datecreated;
    }

    @XmlTransient
    public Collection<UserTransactions> getUserTransactionsCollection() {
        return userTransactionsCollection;
    }

    public void setUserTransactionsCollection(Collection<UserTransactions> userTransactionsCollection) {
        this.userTransactionsCollection = userTransactionsCollection;
    }

    @Override
    public int hashCode() {
        int hash = 0;
        hash += (id != null ? id.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object object) {
        // TODO: Warning - this method won't work in the case the id fields are not set
        if (!(object instanceof TransactionType)) {
            return false;
        }
        TransactionType other = (TransactionType) object;
        if ((this.id == null && other.id != null) || (this.id != null && !this.id.equals(other.id))) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "com.afrinnova.agentportal.entities.TransactionType[ id=" + id + " ]";
    }
    
}
